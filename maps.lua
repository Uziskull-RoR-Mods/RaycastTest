local done = false
function prepMapTextures()
    for _, map in ipairs(mapList) do
        for i = -2, #map.palette.src do
            local sprList = map.palette.src[i]
            if sprList ~= nil then
                map.palette[i] = {}
                for j = 1, 2 do
                    if sprList[j] ~= nil then
                        map.palette[i][j] = {}
                        for k = 1, TEXTURE_SIZE do
                            local surf = Surface.new(1, TEXTURE_SIZE)
                            graphics.setTarget(surf)
                            sprList[j]:draw(1 - k, 0)
                            map.palette[i][j][k] = surf:createSprite(0, 0)
                            graphics.resetTarget()
                            surf:free()
                        end
                    end
                end
            end
        end
    end
    
    done = true
end